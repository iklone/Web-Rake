
public class ScrapeResult {
	int flag;
	String element;
	
	public ScrapeResult() {
		this.element = null;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public String getElement() {
		return element;
	}

	public void setResult(String element) {
		this.element = element;
	}
}
